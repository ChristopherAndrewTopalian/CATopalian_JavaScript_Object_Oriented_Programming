// modifying_a_property_console_log.js

// object to hold jane's name and score
let jane = 
{
    name: 'Jane',
    score: 98
};

// change jane's score to 100
jane.score = 100;

// log the name and score to console
console.log(jane.name + ' now has a score of ' + jane.score);

//----//

/*
Jane now has a score of 100
*/

