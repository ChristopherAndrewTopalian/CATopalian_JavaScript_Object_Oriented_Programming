// add_a_new_property_console_log

// object to hold jane's name and score
let jane = 
{
    name: 'Jane',
    score: 98
};

// add a new property called level
jane.level = 17;

// log the name and level to console
console.log(jane.name + ' is at level ' + jane.level);

//----//

/*
Jane is at level 17
*/

