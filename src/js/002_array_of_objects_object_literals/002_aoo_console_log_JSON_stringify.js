// aoo_console_log_JSON_stringify.js

// create an array to store objects
let people = [
    // object to hold Jane's name and score
    {
        name: 'Jane',
        score: 98
    },
    // object to hold Tabitha's name and score
    {
        name: 'Tabitha',
        score: 95
    }
];

// log the stringified array of objects to console
console.log(JSON.stringify(people));

//----//

/*
[{"name":"Jane","score":98},{"name":"Tabitha","score":95}]
*/

