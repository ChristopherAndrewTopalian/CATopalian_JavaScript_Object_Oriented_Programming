// aoo_console_table.js

// create an array to store objects
let people = [
    // object to hold Jane's name and score
    {
        name: 'Jane',
        score: 98
    },
    // object to hold Tabitha's name and score
    {
        name: 'Tabitha',
        score: 95
    }
];

// log the array of objects to console table
console.table(people);

//----//

/*
(index)    name     score
    0         Jane         98
    1         Tabitha     95
*/

