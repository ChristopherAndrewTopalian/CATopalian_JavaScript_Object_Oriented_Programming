// aoo_push_object_to_array_console_log.js

// create an empty array to store people
let people = [];

// object to hold jane's name and score
let jane = 
{
    name: 'Jane',
    score: 98
};

// add entry object to people array
people.push(jane);

// log the jane object to the console
console.log(jane);

//----//

/*
Object { name: "Jane", score: 98 }
name: "Jane"
score: 98
*/

